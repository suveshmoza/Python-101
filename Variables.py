# In Python, a variable is a name that represents a value.
# The value can be of any data type such as integer, float, string, list, tuple, dictionary, etc.
# Variables in Python are dynamically typed, which means that the data
# type of a variable can change during the execution of a program.

# Creating variables
firstName = "Suvesh"
lastName = "Moza"
age = 21

# Using the variable
print(firstName, lastName, age)

# Changing the value of a variable
lastName = ""
age = 22

# Using the variable again
print(firstName, lastName, age)
