# Type conversion, also known as typecasting,
# is the process of converting the data type
# of a value from one type to another.

# Integer to float
x = 5
y = float(x)
print(y)

# Float to integer
x = 5.5
y = int(x)
print(y)

# String to integer
x = "10"
y = int(x)
print(y)

# Integer to string
x = 10
y = str(x)
print(y)

# String to float
x = "5.5"
y = float(x)
print(y)
